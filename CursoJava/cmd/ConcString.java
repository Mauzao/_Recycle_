//Concatenaçao de String

public class ConcString
	{
	static public void main(String args[]){
		String sigla = "SOO-I";
		String nome = "Sistema Orientado a Objetos I";
		String titulo = sigla + "-" + nome;
		
		//Esse comando imprimira na tela a frase
		//SOO-I Sistema Orientado a Objetos I
		
		System.out.println(titulo);
	}
		}