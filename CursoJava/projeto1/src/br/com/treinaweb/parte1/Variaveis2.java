package br.com.treinaweb.parte1;

public class Variaveis2 {
	public static void main(String[] args) {
		int i = 9;
		double d = 3.14;
		char C = 'W';
		boolean b = false;
		
		System.out.println("1 = " + i);
		System.out.println("d = " + d);
		System.out.println("C = " + C);
		System.out.println("b = " + b);	
	}

}
