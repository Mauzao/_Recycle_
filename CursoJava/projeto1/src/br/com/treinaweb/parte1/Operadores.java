package br.com.treinaweb.parte1;

public class Operadores {
	
	public static void main(String[] args) {
		int i;
		//operador binario onde 1 é o operando
		i = 0;
		System.out.println(i);
		
		//Operador unario on 1 é o operando
		i++;
		System.out.println(i);
		System.out.println(i > 0? "Maior" : "Menor");
	}

}
